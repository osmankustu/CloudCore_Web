"use client";
import Link from "next/link";
import { useRouter, useSearchParams } from "next/navigation";
import React, { useState } from "react";
import { FaArrowLeft } from "react-icons/fa";

import Checkbox from "@/components/form/input/Checkbox";
import Input from "@/components/form/input/InputField";
import Label from "@/components/form/Label";
import Button from "@/components/ui/button/Button";
import { Modal } from "@/components/ui/modal";
import { useModal } from "@/core/hooks/useModal";
import { useRequestAction } from "@/core/hooks/useRequestAction";

import { useUserStore } from "../../account/store/useUserStore";
import { LoginModel } from "../model/LoginModel";
import { useAuthStore } from "../store/useAuthStore";

export default function SignInForm() {
  const router = useRouter();
  const searchParams = useSearchParams();
  const callbackUrl = searchParams?.get("callbackUrl") || "/management";
  const [showPassword, setShowPassword] = useState(false);
  const [isChecked, setIsChecked] = useState(false);
  const { isOpen, openModal, closeModal } = useModal();
  const [title, setTitle] = useState<string>();
  const { fetchAccountRoles, fetchUser, fetchIdentityUser } = useUserStore();
  const { login } = useAuthStore();

  const [loginForm, setLoginForm] = useState<LoginModel>({
    email: "",
    password: "",
    authenticatorCode: "",
  });

  const { run } = useRequestAction();

  const handleChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    setLoginForm(prev => ({ ...prev, [e.target.name]: e.target.value }));
  };

  const handleLogin = () => {
    run(async () => {
      const response = await login(loginForm);

      switch (response.status) {
        case "success":
          await fetchUser();
          await fetchIdentityUser();
          //fetchAccountRoles();

          router.replace(callbackUrl);
          break;

        case "mfa_email":
        case "mfa_otp":
          setTitle(response.message || "");
          openModal();
          break;
      }
    });
  };

  return (
    <>
      <div className="flex w-full flex-1 flex-col lg:w-1/2">
        <div className="mx-auto mb-5 w-full max-w-md sm:pt-10">
          <Link
            href="/"
            className="inline-flex items-center text-sm text-gray-500 transition-colors hover:text-gray-700 dark:text-gray-400 dark:hover:text-gray-300"
          >
            <FaArrowLeft />
            <p className="pl-2">Geri dön</p>
          </Link>
        </div>
        <div className="mx-auto flex w-full max-w-md flex-1 flex-col justify-center">
          <div>
            <div className="mb-5 sm:mb-8">
              <h1 className="text-title-sm sm:text-title-md mb-2 font-semibold text-gray-800 dark:text-white/90">
                Giriş Yap
              </h1>
              <p className="text-sm text-gray-500 dark:text-gray-400">
                E-posta adresinizi veya telefon numaranızı kullanarak giriş yapabilirsiniz!
              </p>
            </div>
            <div>
              <div className="grid grid-cols-1 gap-3 sm:grid-cols-2 sm:gap-5">
                <button className="inline-flex items-center justify-center gap-3 rounded-lg bg-gray-100 px-7 py-3 text-sm font-normal text-gray-700 transition-colors hover:bg-gray-200 hover:text-gray-800 dark:bg-white/5 dark:text-white/90 dark:hover:bg-white/10">
                  <svg
                    width="20"
                    height="20"
                    viewBox="0 0 20 20"
                    fill="none"
                    xmlns="http://www.w3.org/2000/svg"
                  >
                    <path
                      d="M18.7511 10.1944C18.7511 9.47495 18.6915 8.94995 18.5626 8.40552H10.1797V11.6527H15.1003C15.0011 12.4597 14.4654 13.675 13.2749 14.4916L13.2582 14.6003L15.9087 16.6126L16.0924 16.6305C17.7788 15.1041 18.7511 12.8583 18.7511 10.1944Z"
                      fill="#4285F4"
                    />
                    <path
                      d="M10.1788 18.75C12.5895 18.75 14.6133 17.9722 16.0915 16.6305L13.274 14.4916C12.5201 15.0068 11.5081 15.3666 10.1788 15.3666C7.81773 15.3666 5.81379 13.8402 5.09944 11.7305L4.99473 11.7392L2.23868 13.8295L2.20264 13.9277C3.67087 16.786 6.68674 18.75 10.1788 18.75Z"
                      fill="#34A853"
                    />
                    <path
                      d="M5.10014 11.7305C4.91165 11.186 4.80257 10.6027 4.80257 9.99992C4.80257 9.3971 4.91165 8.81379 5.09022 8.26935L5.08523 8.1534L2.29464 6.02954L2.20333 6.0721C1.5982 7.25823 1.25098 8.5902 1.25098 9.99992C1.25098 11.4096 1.5982 12.7415 2.20333 13.9277L5.10014 11.7305Z"
                      fill="#FBBC05"
                    />
                    <path
                      d="M10.1789 4.63331C11.8554 4.63331 12.9864 5.34303 13.6312 5.93612L16.1511 3.525C14.6035 2.11528 12.5895 1.25 10.1789 1.25C6.68676 1.25 3.67088 3.21387 2.20264 6.07218L5.08953 8.26943C5.81381 6.15972 7.81776 4.63331 10.1789 4.63331Z"
                      fill="#EB4335"
                    />
                  </svg>
                  E-Posta
                </button>
                <button className="inline-flex items-center justify-center gap-3 rounded-lg bg-gray-100 px-7 py-3 text-sm font-normal text-gray-700 transition-colors hover:bg-gray-200 hover:text-gray-800 dark:bg-white/5 dark:text-white/90 dark:hover:bg-white/10">
                  <svg
                    width="21"
                    className="fill-current"
                    height="20"
                    viewBox="0 0 21 20"
                    fill="none"
                    xmlns="http://www.w3.org/2000/svg"
                  >
                    <path d="M15.6705 1.875H18.4272L12.4047 8.75833L19.4897 18.125H13.9422L9.59717 12.4442L4.62554 18.125H1.86721L8.30887 10.7625L1.51221 1.875H7.20054L11.128 7.0675L15.6705 1.875ZM14.703 16.475H16.2305L6.37054 3.43833H4.73137L14.703 16.475Z" />
                  </svg>
                  Telefon Numarası
                </button>
              </div>

              <div className="mt-5 space-y-6">
                <div>
                  <Label>
                    E-Posta <span className="text-error-500">*</span>{" "}
                  </Label>
                  <Input
                    placeholder="info@gmail.com"
                    type="email"
                    name="email"
                    onChange={handleChange}
                  />
                </div>
                <div>
                  <Label>
                    Şifre <span className="text-error-500">*</span>{" "}
                  </Label>
                  <div className="relative">
                    <Input
                      type={showPassword ? "text" : "password"}
                      placeholder="Enter your password"
                      name="password"
                      onChange={handleChange}
                    />
                    <span
                      onClick={() => setShowPassword(!showPassword)}
                      className="absolute top-1/2 right-4 z-30 -translate-y-1/2 cursor-pointer"
                    >
                      {showPassword ? (
                        <Label className="fill-gray-500 dark:fill-gray-400">Gizle</Label>
                      ) : (
                        <Label className="fill-gray-500 dark:fill-gray-400">Göster</Label>
                      )}
                    </span>
                  </div>
                </div>
                <div className="flex items-center justify-between">
                  <div className="flex items-center gap-3">
                    <Checkbox checked={isChecked} onChange={setIsChecked} />
                    <span className="text-theme-sm block font-normal text-gray-700 dark:text-gray-400">
                      Beni hatırla
                    </span>
                  </div>
                  <Link
                    href="/reset-password"
                    className="text-brand-500 hover:text-brand-600 dark:text-brand-400 text-sm"
                  >
                    Şifremi unuttum?
                  </Link>
                </div>
                <div>
                  <Button
                    data-track="login-btn"
                    className="w-full"
                    size="sm"
                    onClick={() => handleLogin()}
                  >
                    Giriş Yap
                  </Button>
                </div>
              </div>
            </div>
          </div>
        </div>
      </div>
      <Modal isOpen={isOpen} onClose={closeModal} className="m-4 max-w-[450px]">
        <div className="no-scrollbar relative w-full max-w-[900px] overflow-y-auto rounded-3xl bg-white p-4 lg:p-11 dark:bg-gray-900">
          <div className="px-2 pr-14">
            <h4 className="mb-2 text-center text-2xl font-semibold text-gray-800 dark:text-white/90">
              Doğrulama Gerekli
            </h4>
          </div>
          <h5 className="bg-white p-4 text-center text-2xl font-semibold dark:bg-gray-900">
            {title}
          </h5>
          <div>
            <Label className="text-center">Doğrulama Kodu</Label>
            <Input
              type="text"
              name="AuthenticatorCode"
              onChange={handleChange}
              className="text-center"
              max="6"
              min="6"
            />
          </div>
        </div>
        <div className="mt-6 flex items-center gap-3 px-2 lg:justify-center">
          <Button size="sm" variant="outline" onClick={closeModal}>
            Kapat
          </Button>
          <Button size="sm" onClick={() => handleLogin()}>
            Doğrula
          </Button>
        </div>
        <br />
      </Modal>
    </>
  );
}
